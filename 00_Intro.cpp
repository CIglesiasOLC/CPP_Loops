// ******************************** LOOPING ***********************************
// LOOPING - means to repeat a series  of statements based on the provided condition.
// ITERATION - one pass in the loop.
//
// *********************** PHASES OF LOOPING STATEMENT *************************
// 1) INITIALIZATION - the phase where a variable called "counter" is set an intial value.                     
// 2) CONDITION - the phase where an expression is evaluated which dictates whether the loop
//                should stop or continue.
// 3) BODY - the phase where the statements inside the loop are executed.
// 4) INCREMENT / DECREMENT - the phase where the "counter" is incremented or decremented.
//
// *********************** LOOPING STATEMENTS IN C++ ****************************
// 1) WHILE LOOP
// 2) DO WHILE LOOP
// 3) FOR LOOP
// *****************************************************************************